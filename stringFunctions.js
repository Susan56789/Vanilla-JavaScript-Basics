/*
CharAt(x) : will return the character at
the x position within the string
*/
var str = 'Hello World! Let us learn';
console.log(str.charAt(9));

/*
concat() : combines one or more strings
into existing one
*/
var fname = "Susan";
var message = fname.concat(" is learning"," JavaScript !");
console.log(message);

/*
indexof - searches and (if found ) returns the index number of the searched
character or substring within the string
*/
var sentence = "Hi my name is Mitchelle !";
console.log(sentence.indexOf("name"));